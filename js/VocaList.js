var $ul=null;
function pageMaker(){
    //alert(typeof(nowPage));
    $pageMark.children().each(function(){
      $(this).remove();
    });

    $ul= $("<ul class='paginaiton'/>");

    $pageMark.append($ul);

    if(nowPage <= 10){
      $pageMark.children().append("<li>▣</li>");
    }
    else{
      $pageMark.children().append("<li><a href='#' class='pageBtn' style='color:black'><<</a></li>");
    }



    if( nowBlock < totalBlock ){
      for(var i= ((nowBlock-1)*10)+1; i<=(nowBlock)*10; i++){
          $pageMark.children().append("<li><a href='#' class='pageBtn' style='color:black'>"+i+"</a></li>");
      }
      $pageMark.children().append("<li><a href='#' class='pageBtn' style='color:black'>>></a></li>");
    }
    else{
      for(var i= ((nowBlock-1)*10)+1; i<=totalPage; i++){
          $pageMark.children().append("<li><a href='#' class='pageBtn' style='color:black'>"+i+"</a></li>");
      }
      $pageMark.children().append("<li>▣</li>");
    }
    initEvent();
}


function initEvent(){
    var $liList = $("ul.paginaiton li a");
    $liList.click(function(){
        if( $(this).text() == ">>" ){
          nowBlock +=1;
          nowPage = ((nowBlock-1)*10)+1;
        }
        else if($(this).text() == "<<"){
          nowBlock -=1;
          nowPage = nowPage-10;
        }
        else{
          nowPage = parseInt($(this).text());
        }

        $inputList.eq(1).val(nowPage);
        $inputList.eq(2).val(nowBlock);

        wordMaker();
    });
}
